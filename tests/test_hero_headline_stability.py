@pytest.mark.tooling
def test_followup_without_previous_run_does_not_pin(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    """No completed run for the site -> nothing was ever rendered -> no pin
    (the next build keeps the same freedom an init build has)."""
    _, followup_pi = _init_then_followup(
        monkeypatch, tmp_path, followup_prompt=NEUTRAL_FOLLOWUP, write_run=False
    )
    assert "heroHeadline" not in followup_pi["company"]


@pytest.mark.tooling
def test_pin_falls_back_to_company_name_when_blueprint_lacks_hero_block(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    """A run whose generation package carries no home.hero block rendered
    company.name as the H1 (the renderer's template fallback) — that is the
    value that must carry forward."""
    initial_pi, followup_pi = _init_then_followup(
        monkeypatch, tmp_path, followup_prompt=NEUTRAL_FOLLOWUP, run_headline=None
    )
    assert (
        followup_pi["company"]["heroHeadline"]
        == initial_pi["company"]["name"]
    )


# ---------------------------------------------------------------------------
# 5. --base-run-id: the pin follows the run the operator iterates from
# ---------------------------------------------------------------------------


@pytest.mark.tooling
def test_base_run_id_pins_that_runs_headline(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    monkeypatch.delenv("OPENAI_API_KEY", raising=False)
    output_dir = tmp_path / "prompt-inputs"
    runs_dir = tmp_path / "runs"
    generate(
        INIT_PROMPT,
        output_dir=output_dir,
        site_id=SITE_ID,
        project_id=PROJECT_ID,
    )
    old_run = _write_previous_run(
        runs_dir, run_id="run-old", headline="Den gamla rubriken", version=1
    )
    newer_run = _write_previous_run(
        runs_dir, run_id="run-new", headline="Den nyaste rubriken", version=1
    )
    past = time.time() - 120
    os.utime(old_run, (past, past))
    future = time.time() + 60
    os.utime(newer_run, (future, future))
    followup_pi, _, _, _ = generate_followup(
        NEUTRAL_FOLLOWUP,
        site_id=SITE_ID,
        output_dir=output_dir,
        runs_dir=runs_dir,
        base_run_id="run-old",
    )
    assert followup_pi["company"]["heroHeadline"] == "Den gamla rubriken", (
        "iterating from a historical run must carry THAT run's rendered H1"
    )
