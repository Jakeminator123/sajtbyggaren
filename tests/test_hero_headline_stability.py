@pytest.mark.tooling
def test_existing_explicit_hero_headline_is_not_overwritten_by_pin(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    """An operator-set heroHeadline from an earlier follow-up must survive a
    later neutral follow-up untouched, even when a newer run carries a
    different blueprint headline."""
    monkeypatch.delenv("OPENAI_API_KEY", raising=False)
    output_dir = tmp_path / "prompt-inputs"
    runs_dir = tmp_path / "runs"
    generate(
        INIT_PROMPT,
        output_dir=output_dir,
        site_id=SITE_ID,
        project_id=PROJECT_ID,
    )
    _write_previous_run(runs_dir, run_id="run-0001")
    explicit_pi, _, _, _ = generate_followup(
        'Byt rubriken till "Palma målar bäst"',
        site_id=SITE_ID,
        output_dir=output_dir,
        runs_dir=runs_dir,
    )
    assert explicit_pi["company"]["heroHeadline"] == "Palma målar bäst"
    newer_run = _write_previous_run(
        runs_dir, run_id="run-0002", headline="Regenererad rubrik v2", version=2
    )
    future = time.time() + 60
    os.utime(newer_run, (future, future))
    neutral_pi, _, _, _ = generate_followup(
        NEUTRAL_FOLLOWUP,
        site_id=SITE_ID,
        output_dir=output_dir,
        runs_dir=runs_dir,
    )
    assert neutral_pi["company"]["heroHeadline"] == "Palma målar bäst"


